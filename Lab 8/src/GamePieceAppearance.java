
public class GamePieceAppearance {
//instance variables
	private Color color;
	private Shape shape;
//Constructor	
	public GamePieceAppearance(Color color1, Shape shape1) {
		this.color = color1;
		this.shape = shape1;
	}
//Getter's
	public Color getColor() {
		return color;
	}

	public Shape getShape() {
		return shape;
	}
	
}
